#include "NuSDSimParameters.hh"
#include "NuSDSimParametersMessenger.hh"

#include "G4RunManager.hh"
#include "G4StateManager.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

NuSDSimParameters* NuSDSimParameters::fInstance = 0;

NuSDSimParameters::NuSDSimParameters():
fMessenger{nullptr},
fIsOpticalPhysicsOn{true} 
{
  fMessenger = new NuSDSimParametersMessenger(this);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

NuSDSimParameters::~NuSDSimParameters()
{
  delete fMessenger;  
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

NuSDSimParameters* NuSDSimParameters::GetInstance()
{
  if (fInstance == 0)
    {
      fInstance = new NuSDSimParameters();
    }
  return fInstance;
}

